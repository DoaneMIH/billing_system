// Criteria: 60+ days overdue OR status = 'hold_disconnection' with 45+ days overdue

$sql = "SELECT 
        c.customer_id,
        c.account_number,
        c.subscriber_name,
        c.address,
        c.tel_no,
        a.area_name,
        c.status,
        c.monthly_fee,
        COUNT(DISTINCT b.billing_id) as unpaid_bills_count,
        MIN(b.due_date) as oldest_due_date,
        MAX(p.payment_date) as last_payment_date,
        SUM(b.net_amount - COALESCE((SELECT SUM(amount_paid) FROM payments WHERE billing_id = b.billing_id), 0)) as total_balance
        FROM customers c
        LEFT JOIN areas a ON c.area_id = a.area_id
        JOIN billings b ON c.customer_id = b.customer_id
        LEFT JOIN payments p ON c.customer_id = p.customer_id
        WHERE b.status IN ('unpaid', 'partial')
        AND c.status != 'disconnected'";

if ($area_filter > 0) {
    $sql .= " AND c.area_id = $area_filter";
}

$sql .= " GROUP BY c.customer_id, c.account_number, c.subscriber_name, c.address, c.tel_no, a.area_name, c.status, c.monthly_fee
          HAVING total_balance > 0
          ORDER BY oldest_due_date ASC";

$all_customers = $conn->query($sql);

// Filter for disconnection candidates
$disconnection_candidates = [];
$total_to_disconnect = 0;
$total_balance_loss = 0;

while ($row = $all_customers->fetch_assoc()) {
    $days_overdue = $row['oldest_due_date'] ? 
        floor((strtotime('now') - strtotime($row['oldest_due_date'])) / (60 * 60 * 24)) : 0;
    
    // Criteria for disconnection
    $should_disconnect = false;
    $reason = '';
    
    if ($days_overdue >= 60 && $row['status'] == 'active') {
        $should_disconnect = true;
        $reason = '60+ days overdue';
    } elseif ($days_overdue >= 45 && $row['status'] == 'hold_disconnection') {
        $should_disconnect = true;
        $reason = 'Hold period expired (45+ days)';
    }
    
    if ($should_disconnect) {
        $row['days_overdue'] = $days_overdue;
        $row['disconnection_reason'] = $reason;
        $disconnection_candidates[] = $row;
        $total_to_disconnect++;
        $total_balance_loss += $row['total_balance'];
    }
}
